#include<stdio.h>
int main(){

    int    a = 5;    // a and b is a container which stores the data 
    float  b = 5.5;
    double d = 5.5555555; 
    char   c = 'a';
     

    printf("%d\n",a);
    printf("%f\n",b);   // %f is formate specifier
    printf("%lf\n",d);
    printf("%c\n",c);

    return 0;

}